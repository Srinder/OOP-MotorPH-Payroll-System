   private void jButtonTimeoutActionPerformed(java.awt.event.ActionEvent evt) {
        if (currentUser != null) {
        repository.AttendanceRepository attendanceRepo = new repository.AttendanceRepository();
        
        // 1. Get current date and time
        String today = java.time.LocalDate.now().format(java.time.format.DateTimeFormatter.ofPattern("MM/dd/yyyy"));
        String now = java.time.LocalTime.now().format(java.time.format.DateTimeFormatter.ofPattern("H:mm"));
        String empId = String.valueOf(currentUser.getEmployeeNumber());

        // 2. Check if the user has even timed in yet today
        if (!attendanceRepo.hasLoggedToday(empId, today)) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "No Time In record found for today.",
                    "Missing Log",
                    javax.swing.JOptionPane.WARNING_MESSAGE);
        } else {
            // 3. Create the record object with the logout time
            model.AttendanceRecord record = new model.AttendanceRecord(
                    empId,
                    currentUser.getLastName(),
                    currentUser.getFirstName(),
                    today,
                    "", // We don't need to pass Time In here, the update method handles it
                    now  // This is our LogOut time
            );

            // 4. Update using your repository
            attendanceRepo.update(record);
            javax.swing.JOptionPane.showMessageDialog(this, "Time Out recorded at " + now);
        }
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "User session not found.");
    }
}                   

