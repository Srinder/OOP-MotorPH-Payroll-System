    private void jButtonEmployeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEmployeeActionPerformed
                                    
    String role = User.getLoggedInUser().getRole();

    if ("EMPLOYEE".equalsIgnoreCase(role)) {
        int empId = Integer.parseInt(User.getLoggedInUser().getEmployeeId());
        EditEmpInfo editInfo = new EditEmpInfo(empId, true);
        editInfo.setVisible(true);
        editInfo.setLocationRelativeTo(null);
    } else if ("SUPPORT".equalsIgnoreCase(role)) {
        String supervisorId = User.getLoggedInUser().getEmployeeId();
        EmployeeTable filteredTable = new EmployeeTable(supervisorId, "Employee Information");
        filteredTable.setVisible(true);
        filteredTable.setLocationRelativeTo(null);
    } else {
        femptable.setVisible(true);
        femptable.setLocationRelativeTo(null);
    }
    }//GEN-LAST:event_jButtonEmployeeActionPerformed

    private void jButtonPayslipActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPayslipActionPerformed
    String role = User.getLoggedInUser().getRole();

    if ("ADMIN".equalsIgnoreCase(role)) {
        // üë®‚Äçüíº Admin users get full employee list view
        EmployeeTable employeeTable = new EmployeeTable(null, "Employee Payslip");
        employeeTable.setVisible(true);
        employeeTable.setLocationRelativeTo(null);
        return;
    }

    if ("EMPLOYEE".equalsIgnoreCase(role) || 
        "SUPPORT".equalsIgnoreCase(role) || 
        "HR".equalsIgnoreCase(role)) {
        
        String empId = User.getLoggedInUser().getEmployeeId();
        new Payslip(empId).setVisible(true);
    } else {
        String input = JOptionPane.showInputDialog(
            this,
            "Enter Employee Number:",
            "Generate Payslip",
            JOptionPane.QUESTION_MESSAGE
        );

        if (input != null && !input.trim().isEmpty()) {
            new Payslip(input.trim()).setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Employee Number is required.", "Input Error", JOptionPane.WARNING_MESSAGE);
        }
    }
    }//GEN-LAST:event_jButtonPayslipActionPerformed

